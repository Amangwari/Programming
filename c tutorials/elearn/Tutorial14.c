#include <stdio.h>
// TYPECASTING SYNTAX
 // (TYPE) VALUE;

int main()
{   
    int a = 3;
    float b = (float)54/5; // = ke bad int ki jaghe float islie likha hai kuki humne integer ko float me convert krna hai
    printf("The value of a is %f\n", b);   //%d ki jaghe %f likhenge
    return 0;
}











