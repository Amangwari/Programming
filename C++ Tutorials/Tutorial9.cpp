// Comparision operators
#include <iostream>
using namespace std;
main()
{
    int a = 5, b = 6;
    cout << "The value of a == b is " << (a == b);
    cout << " \nThe value of a != b is " << (a != b);
    cout << " \nThe value of a >= b is " << (a >= b);
    cout << " \nThe value of a <= b is " << (a <= b);
    cout << " \nThe value of a > b is " << (a > b);
    cout << " The value of a < b is " << (a < b);
    return 0;
}