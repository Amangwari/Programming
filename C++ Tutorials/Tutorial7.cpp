// Operators in C++
// Arithmatic operator
#include <iostream>
using namespace std;
main()
{
    int a = 4, b = 5;
    cout << "Operators in C++" << endl;
    cout << "Following are the types of operators in C++" << endl;
    // Arithmatic Operators
    cout << " The value of a + b is : " << a + b << endl;
    cout << " The value of a - b is : " << a - b << endl;
    cout << " The value of a / b is : " << a / b << endl;
    cout << " The value of a * b is : " << a * b << endl;
    cout << " The value of a % b is : " << a % b << endl;
    cout << " The value of a++ is : " << a++ << endl;
    cout << " The value of a-- is : " << a-- << endl;
    cout << " The value of ++a is : " << ++a << endl;
    cout << " The value of --a is  : " << --a << endl;
    return 0;
}