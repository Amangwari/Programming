#include <stdio.h>
int main()
{
    int a, b;                                 // OPERATORS IN C
    a = 45;                                   // RELATIONAL OPERATOR
    b = 45;                                   

    printf("a + b =% d\n", a == b);
    printf("a + b =% d\n", a != b);
    printf("a + b =% d\n", a > b);
    printf("a + b =% d\n", a < b);
    printf("a + b =% d\n", a >= b);
    printf("a + b =% d\n", a <= b);

    return 0;
}
