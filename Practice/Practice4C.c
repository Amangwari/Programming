#include <stdio.h>
int main()
{
    int a, b;                                 // OPERATORS IN C
    a = 45;                                   // RELATIONAL OPERATOR
    b = 45;                                   // LOGICAL OPERATIONS
                                              // && = AND
    printf("a && b =% d\n", a && b);          // || = OR
    printf("a || b =% d\n", a || b);          // ! = NOT
    printf("a ! b =% d\n", a && b );          // IF WE CHANGE VALUE OF INT A OR B THEN RESULT CHANGES
    
    return 0;
}
