#include <stdio.h>

int main()
{
    int a, b;
    a = 10;
    b = 15;
                                                         // ASSIGNMENT OPERATOR (SOME DOUBTS)
    printf("a += b = %d\n", a += b);
    printf("a = b = %d\n", a = b);
    printf("a -= b = %d\n", a -= b);
    printf("a *= b = %d\n", a *= b);
    printf("a /= b = %d\n", a /= b);

    return 0;
}
